CREATE PROCEDURE [dbo].[spValidarUsuario]
@Email varchar(50),
@Password varchar(100)
as
begin
	SELECT id, email, pass, nombre, apellido, UrlImagenPerfil, admin
	FROM USERS 
	WHERE email = @Email AND pass = @Password
