CREATE PROCEDURE [dbo].[spAgregarUsuario]
    @Nombre VARCHAR(50),
    @Apellido VARCHAR(50),
    @Email VARCHAR(100),
    @Pass VARCHAR(20),
    @Admin BIT
AS
BEGIN
    SET NOCOUNT ON;

    INSERT INTO USERS (nombre, apellido, email, pass, admin)
    VALUES (@Nombre, @Apellido, @Email, @Pass, @Admin);

    SELECT SCOPE_IDENTITY() AS Id;
END
